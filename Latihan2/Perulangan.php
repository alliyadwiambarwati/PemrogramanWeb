//pernyataan For

<?php
for ($i = 1; $i <=5; $i++){
    echo "Iterasi ke $i <br>";
}
?>



//pernyataan while

<?php
$counter = 1;
while ($counter <=5){
    echo "Iterasi ke-$counter <br>";
    $counter++;
}
?>



//pernyataan do-while

<?php
$counter = 1;
 do{
    echo "Iterasi ke-$counter <br>";
    $counter++;
 } while ($counter <=5);
 ?>